class Alerta {
    constructor(id, tipoAlerta, descripcion, fecha, hora, opcionesRepetciones) {
        this.id = id;
        this.tipoAlerta = tipoAlerta;
        this.descripcion = descripcion;
        this.fecha = fecha;
        this.hora = hora;
        this.opcionesRepetciones = opcionesRepetciones;
    }
}